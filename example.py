#Задачи на break и continue
#1. Напишите программу, которая ищет наибольший (не равный 1) общий делитель
# двух чисел, введённых пользователем (num_1, num_2), и выводит его на экран.
# Если общих делителей нет, выводит на экран "Общих делителей не найдено".

num_1 = int(input())
num_2 = int(input())
while num_1 != 0 and num_2 != 0:
    if num_1 > num_2:
        num_1 = num_1 % num_2
    else:
        num_2 = num_2 % num_1
        if num_1 == 1 or num_2 == 1:
            break
print(num_1 + num_2)

# 2. Напишите программу, которая проверяет, является ли введённое число (number)
# простым. Если число простое, то программа выводит "Простое", иначе
# "Не является простым".
print("Программа по проверке простого числа:")

number = int(input("Введите число: "))
k = 0

for i in range(2, number - 1):
    if number % i == 0:
        k += 1

if k == 0:
    print(number, 'простое число')
else:
    print(number, 'Не является простым')

