# 2. Пассажирский лифт имеет ограничение на перевозку — не более 400 кг
# единовременно. Пусть на вход программе поступают веса (weight) входящих
# в лифт людей, а в случае, когда возникает перевес, программа выдаёт
# предупреждение: "Перевес ... кг". Например, при перевесе в 15 кг должно
# быть выведено сообщение: "Перевес 15 кг".
weight_max = 400
weight = int(input("Введите Ваш вес: "))
while weight < weight_max:
    weight_now = int(input("Введите Ваш вес: "))
    weight += weight_now
    print("Общий вес: ", weight)
else:
    print("Перевес: ", weight - weight_max, "кг")


